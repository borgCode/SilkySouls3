using System;
using SilkySouls3.Models;

namespace SilkySouls3.Data
{
    public static class ItemDropTemplate
    {
        private const int PosX        = 0x10;
        private const int PosY        = 0x14;
        private const int PosZ        = 0x18;
        private const int Angle       = 0x1C;
        private const int Flags       = 0x28;
        private const int RequestType = 0x34;
        private const int ItemKey     = 0x38;
        private const int Quantity    = 0x3C;

        private static readonly byte[] Bytes =
        [
            0x01, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF,
            0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
            0xB0, 0xA6, 0xE3, 0xC3, 0x2A, 0xB4, 0x44, 0xC3,
            0x81, 0x08, 0x1B, 0xC4, 0x05, 0x81, 0x05, 0xC0,
            0x58, 0x00, 0xA0, 0x00, 0x00, 0x00, 0x00, 0x00,
            0xD0, 0x01, 0x40, 0x40, 0xFF, 0xFF, 0xFF, 0xFF,
            0xFF, 0xFF, 0xFF, 0xFF, 0x01, 0x00, 0x00, 0x00,
            0x26, 0x01, 0x00, 0x40, 0x01, 0x00, 0x00, 0x00,
            0xFF, 0xFF, 0xFF, 0xFF, 0x90, 0xA2, 0xC5, 0x9F,
            0xF4, 0x7F, 0x00, 0x00, 0x08, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF,
            0x01, 0x00, 0x00, 0x00, 0x01, 0x92, 0xAF, 0x40,
            0x01, 0x00, 0x00, 0x00, 0x00, 0xF0, 0x14, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x01, 0x01, 0x00, 0x40,
            0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x28, 0xF0, 0x14, 0x00,
            0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x90, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00
        ];

        public static byte[] BuildFor(ItemDrop drop)
        {
            var buf = (byte[])Bytes.Clone();

            BitConverter.GetBytes(drop.Coords.X).CopyTo(buf, PosX);
            BitConverter.GetBytes(drop.Coords.Y).CopyTo(buf, PosY);
            BitConverter.GetBytes(drop.Coords.Z).CopyTo(buf, PosZ);
            BitConverter.GetBytes(drop.Angle).CopyTo(buf, Angle);
            BitConverter.GetBytes(drop.Flags).CopyTo(buf, Flags);
            BitConverter.GetBytes(drop.RequestType).CopyTo(buf, RequestType);
            BitConverter.GetBytes(drop.ItemKey).CopyTo(buf, ItemKey);
            BitConverter.GetBytes(drop.Quantity).CopyTo(buf, Quantity);

            return buf;
        }
    }
}
